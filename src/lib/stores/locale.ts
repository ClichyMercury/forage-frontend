// ── Dictionnaires par code ────────────────────────────────────────────────────

const dicts: Record<LocaleCode, Record<string, string>> = { fr, en }

// ── Store ─────────────────────────────────────────────────────────────────────

const STORAGE_KEY = 'forage_locale'

function createLocaleStore() {
  const initial: LocaleCode =
    typeof localStorage !== 'undefined'
      ? ((localStorage.getItem(STORAGE_KEY) as LocaleCode) ?? 'fr')
      : 'fr'

  const { subscribe, set } = writable<LocaleCode>(initial)

  return {
    subscribe,
    set(code: LocaleCode) {
      if (typeof localStorage !== 'undefined') localStorage.setItem(STORAGE_KEY, code)
      if (typeof document !== 'undefined') document.documentElement.lang = code
      set(code)
    },
  }
}

export const locale = createLocaleStore()

// ── Fonction de traduction ────────────────────────────────────────────────────

export const t = derived(locale, ($locale) => {
  const dict = dicts[$locale] ?? fr

  return (key: string, vars?: Record<string, string | number>): string => {
    let str = dict[key] ?? fr[key] ?? key
    if (vars) {
      for (const [k, v] of Object.entries(vars)) {
        str = str.replaceAll(`{${k}}`, String(v))
      }
    }
    return str
  }
})

export function setLocale(code: LocaleCode) {
  locale.set(code)
}

export function translate(key: string, vars?: Record<string, string | number>): string {
  return get(t)(key, vars)
}

// ── Locale Intl (pour toLocaleDateString / toLocaleString) ────────────────────

// Mapping LocaleCode → tag BCP 47 utilisé par Intl.
// FR : fr-CM (français camerounais). EN : en-GB.
const INTL_LOCALES: Record<LocaleCode, string> = {
  fr: 'fr-CM',
  en: 'en-GB',
}

export const intlLocale = derived(locale, ($locale) => INTL_LOCALES[$locale] ?? 'fr-CM')
